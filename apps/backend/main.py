import base64
import json
import os
import re
import uuid

import httpx
from dotenv import load_dotenv
from fastapi import FastAPI, File, HTTPException, Query, UploadFile
from fastapi.middleware.cors import CORSMiddleware
from openai import AsyncOpenAI
from pydantic import BaseModel

load_dotenv()

app = FastAPI(title="RepairSync Backend")

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_methods=["*"],
    allow_headers=["*"],
)

openai_client = AsyncOpenAI(api_key=os.environ["OPENAI_API_KEY"])

CONVEX_URL = os.environ["CONVEX_URL"].rstrip("/")
CONVEX_DEPLOY_KEY = os.environ.get("CONVEX_DEPLOY_KEY", "")

CONVEX_HEADERS = {
    **({"Authorization": f"Convex {CONVEX_DEPLOY_KEY}"} if CONVEX_DEPLOY_KEY else {}),
    "Content-Type": "application/json",
}

SYSTEM_PROMPT = """You are a neutral AI vehicle damage analyzer. You are not biased toward the driver, insurer, or repair shop. Be precise and honest.

Your task: Examine the vehicle photo and identify every distinct damage zone visible.

For EACH damage zone output:
- zoneName: specific location (e.g. "front bumper driver side" not just "bumper")
- severity: exactly one of "minor" (surface scratches, small dents under 2 inches, cosmetic only), "moderate" (dents over 2 inches, cracked trim, broken lights), or "severe" (structural, frame, or safety-critical damage)
- description: plain English description of what you see
- confidence: float 0.0 to 1.0

Also output:
- vehicleDescription: color, body type, approximate size (e.g. "silver mid-size sedan")
- overallSeverity: "minor", "moderate", or "severe" — worst zone across all
- repairPriority: exactly one of "immediate" (safety risk), "soon" (within weeks), or "when convenient" (cosmetic only)
- summary: 2–3 sentences written directly to the vehicle owner in plain English
- disclaimer: always include exactly "This assessment is generated by AI for informational purposes only. Always obtain quotes from licensed repair facilities before authorizing repairs."

Poor image handling: If the photo is blurry, dark, or too far away — still assess what is visible. Flag affected zones with confidence below 0.6. Tell the user what angle or lighting would help in the summary.

CRITICAL: Output strict JSON only. No markdown. No backticks. No preamble. No explanation outside the JSON object.

JSON schema:
{
  "damageZones": [
    {
      "zoneName": "string",
      "severity": "minor|moderate|severe",
      "description": "string",
      "confidence": 0.0
    }
  ],
  "vehicleDescription": "string",
  "overallSeverity": "minor|moderate|severe",
  "repairPriority": "immediate|soon|when convenient",
  "summary": "string",
  "disclaimer": "string"
}"""


# ---------------------------------------------------------------------------
# Convex helpers
# ---------------------------------------------------------------------------

async def convex_mutation(fn_name: str, args: dict) -> dict:
    """Call a Convex mutation via the HTTP API."""
    async with httpx.AsyncClient(timeout=30) as client:
        resp = await client.post(
            f"{CONVEX_URL}/api/mutation",
            headers=CONVEX_HEADERS,
            json={"path": fn_name, "args": args},
        )
        resp.raise_for_status()
        return resp.json()


async def update_assessment(assessment_id: str, result: dict) -> None:
    await convex_mutation("assessments:updateAssessment", {"id": assessment_id, **result})


async def fail_assessment(assessment_id: str) -> None:
    await convex_mutation("assessments:failAssessment", {"id": assessment_id})


# ---------------------------------------------------------------------------
# Upload endpoint — saves image to /tmp/, returns local path as image_url
# ---------------------------------------------------------------------------

class UploadResponse(BaseModel):
    image_url: str


@app.post("/upload", response_model=UploadResponse)
async def upload_image(
    file: UploadFile = File(...),
    assessment_id: str = Query(...),
):
    """
    Accepts a multipart image upload, saves it to /tmp/, and returns the
    local file path. The analyze endpoint reads this path directly and sends
    the image to GPT-4o Vision as base64 — no Convex storage required.
    """
    image_bytes = await file.read()
    if not image_bytes:
        raise HTTPException(status_code=400, detail="Empty file received")

    tmp_path = f"/tmp/{uuid.uuid4().hex}.jpg"
    with open(tmp_path, "wb") as f:
        f.write(image_bytes)

    print(f"[upload] saved {len(image_bytes)} bytes to {tmp_path}")

    # Update the assessment record with the temp path so it's queryable
    try:
        await convex_mutation(
            "assessments:updateImageUrl",
            {"id": assessment_id, "imageUrl": tmp_path},
        )
    except Exception as e:
        print(f"[upload] warning: could not update imageUrl in Convex: {e}")

    return UploadResponse(image_url=tmp_path)


# ---------------------------------------------------------------------------
# Analyze endpoint
# ---------------------------------------------------------------------------

class AnalyzeRequest(BaseModel):
    assessment_id: str
    image_url: str  # local /tmp/ path written by /upload


@app.post("/analyze")
async def analyze(req: AnalyzeRequest):
    tmp_path = req.image_url
    try:
        # Read the image from disk and encode as base64
        if not os.path.exists(tmp_path):
            raise FileNotFoundError(f"Temp file not found: {tmp_path}")

        with open(tmp_path, "rb") as f:
            image_bytes = f.read()

        b64_image = base64.b64encode(image_bytes).decode("utf-8")
        print(f"[analyze] read {len(image_bytes)} bytes from {tmp_path}, sending to GPT-4o")

        # Send to GPT-4o Vision as base64 data URL
        response = await openai_client.chat.completions.create(
            model="gpt-4o",
            temperature=0.2,
            max_tokens=1500,
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {
                    "role": "user",
                    "content": [
                        {
                            "type": "image_url",
                            "image_url": {
                                "url": f"data:image/jpeg;base64,{b64_image}",
                                "detail": "high",
                            },
                        },
                        {
                            "type": "text",
                            "text": "Analyze the vehicle damage in this photo and return your assessment as JSON.",
                        },
                    ],
                },
            ],
        )

        raw = response.choices[0].message.content or ""
        print(f"[analyze] GPT-4o raw response: {raw[:200]}...")

        # Strip markdown code fences if GPT ignores instructions
        raw = re.sub(r"```(?:json)?", "", raw).strip().strip("`").strip()

        result = json.loads(raw)

        # Validate required fields
        required = ["damageZones", "vehicleDescription", "overallSeverity", "repairPriority", "summary", "disclaimer"]
        for field in required:
            if field not in result:
                raise ValueError(f"Missing field in GPT response: {field}")

        # Write results to Convex
        await update_assessment(req.assessment_id, result)

        return {"status": "complete", "assessment_id": req.assessment_id, **result}

    except Exception as exc:
        print(f"[analyze] error: {exc}")
        try:
            await fail_assessment(req.assessment_id)
        except Exception as e:
            print(f"[analyze] warning: could not call failAssessment: {e}")
        raise HTTPException(status_code=500, detail=str(exc))

    finally:
        # Always clean up the temp file
        if os.path.exists(tmp_path):
            os.remove(tmp_path)
            print(f"[analyze] deleted temp file {tmp_path}")


